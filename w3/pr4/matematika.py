# Funktsii min() i max() nakhodyat naimenshee i naibolshee znachenie
x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

# Funktsiya abs() vozvraschaet absolyutnoe (polozhitelnoe) znachenie chisla
x = abs(-7.25)

print(x)

# Funktsiya pow(x, y) vozvodi kh v stepen y (x^y)
# Primery: 4 v kubu (4 * 4 * 4)
x = pow(4, 3)

print(x)

# Modul math rasshiryaet spisok matematicheskikh funktsiy
# Snachala nuzhno ego importirovat
import math

# Metod math.sqrt() vozvraschaet kvadratnyy koren chisla
x = math.sqrt(64)

print(x)

# math.ceil() okruglyaet vbolshuyu storonu, math.floor() - v menshuyu
x = math.ceil(1.4)  # 2
y = math.floor(1.4) # 1

print(x)
print(y)

# Konstanta math.pi vozvraschaet znachenie chisla 314
x = math.pi

print(x)